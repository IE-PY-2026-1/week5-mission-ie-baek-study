# 1. 고객의 나이를 숫자로 입력받아 변수에 저장
age = int(input("나이를 입력하세요 : "))

# 2. 할인 쿠폰 보유 여부를 대문자 'Y' 또는 'N'으로 입력받아 변수에 저장
coupon = input("할인 쿠폰이 있나요? (Y/N) : ")

# 결과 출력을 위한 구분선 (화면 예시 참고)
print("-" * 40)
print("결과 : ")

# 3. 깜짝 이벤트: 나이가 13세 미만이고(and) 쿠폰이 'Y'인 경우
if age < 13 and coupon == 'Y':
    print("🍿 꼬마 VIP 고객님! 팝콘 무료 증정!")

# 4 & 5. 요금 안내: 나이가 65세 이상이거나(or) 쿠폰이 'Y'인 경우
if age >= 65 or coupon == 'Y':
    print("티켓 요금 : 무료입니다.")
else:
    print("티켓 요금 : 15,000원입니다.")